#ifndef TRACKEDSOURCE_H
#define TRACKEDSOURCE_H

class TrackedSource
{

public:

    /***********************************************************
    * Constructor                                              *
    ***********************************************************/

    TrackedSource(const float _X, const float _Y, const float _Z, const int _trackedSourceID, const int _colorID);
    TrackedSource();

    /***********************************************************
    * Destructor                                               *
    ***********************************************************/

    ~TrackedSource();

    /***********************************************************
    * Accessors                                                *
    ***********************************************************/

    // +-------------------------------------------------------+
    // | Position                                              |
    // +-------------------------------------------------------+

    const float getX();
    const float getY();
    const float getZ();

    // +-------------------------------------------------------+
    // | Tracked source ID                                     |
    // +-------------------------------------------------------+

    const int getTrackedSourceID();

    // +-------------------------------------------------------+
    // | Color ID                                              |
    // +-------------------------------------------------------+

    const int getColorID();


    /***********************************************************
    * Mutators                                                 *
    ***********************************************************/

    // +-------------------------------------------------------+
    // | Position                                              |
    // +-------------------------------------------------------+

    void setX(const float _X);
    void setY(const float _Y);
    void setZ(const float _Z);

    // +-------------------------------------------------------+
    // | Tracked source ID                                     |
    // +-------------------------------------------------------+

    void setTrackedSourceID(const int _trackedSourceID);

    // +-------------------------------------------------------+
    // | Color ID                                              |
    // +-------------------------------------------------------+

    void setColorID(const int _colorID);

private:

    /***********************************************************
    * Fields                                                   *
    ***********************************************************/

    float X, Y, Z;
    int trackedSourceID;
    int colorID;

};


#endif
